package ex_10_For_Loop;

public class Lab107_ForLoop_2 {
    public static void main(String[] args) {
        for (int i = 0; i < 9; i++) {
            System.out.println("int  "+i);
        }
        for (long l = 0; l < 9; l++) {
            System.out.println("long " +l);
        }

        for (float f = 0.0f; f < 9.9; f++) {
            System.out.println("float  "+f);
        }
    }
}