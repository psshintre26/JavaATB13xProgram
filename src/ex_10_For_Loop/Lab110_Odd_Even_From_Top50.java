package ex_10_For_Loop;

public class Lab110_Odd_Even_From_Top50 {
    public static void main(String[] args) {
        //find even numbers from top 50
for (int i = 0;i<=50;i++){
    if (i%2==0){
        System.out.println("even number is "+i );
    } //end of if
    else {
        System.out.println("odd number is "+i);
    }
} //end of for
    } //end of main
} //end of class
