package ex16_Arrays;

public class AllStarPractice {


        public static void main(String[] args) {


            int n = 3;
            for(int k = 0; k < n; k++) {
                for (int j = 0; j < n; j++) {
                    System.out.print("*");

                }
                System.out.println();
            }
        }
    }


